import torch
from torch import nn
from ZSSGAN.model.model_irse import Backbone
# from configs import paths_config


class IDLoss(nn.Module):
    def __init__(self):
        super(IDLoss, self).__init__()
        # print('Loading ResNet ArcFace')
        self.facenet = Backbone(input_size=112, num_layers=50, drop_ratio=0.6, mode='ir_se')
        ir_model_path = '/data1/dxw_data/code/github/iccv2023/jojogan_encoder/initializer/model_ir_se50.pth'
        self.facenet.load_state_dict(torch.load(ir_model_path))
        self.face_pool = torch.nn.AdaptiveAvgPool2d((112, 112))
        self.facenet.eval()

    def extract_feats(self, x):
        x = x[:, :, 35:223, 32:220]  # Crop interesting region
        x = self.face_pool(x)
        x_feats = self.facenet(x)
        return x_feats

    def forward(self, y_hat, y):
        y_feats = self.extract_feats(y)  # Otherwise use the feature from there
        y_hat_feats = self.extract_feats(y_hat)
        y_feats = y_feats.detach()
        loss = 0
        diff_target = y_hat_feats[0].dot(y_feats[0])
        loss += 1 - diff_target
            
        return loss